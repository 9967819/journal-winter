/**
 * Register custom fonts.
 */
function twentyseventeen_fonts_url($fontname = 'Roboto') {
	$fonts_url = '';

	/*
	 * Translators: If there are characters in your language that are not
	 * supported by Libre Franklin, translate this to 'off'. Do not translate
	 * into your own language.
	 */
    $f = _x('on', "$fontname font: on or off", 'twentyseventeen' );

	if ( 'off' !== $f ) {
		$font_families = array();

		$font_families[] = "$fontname:300,300i,400,400i,600,600i,800,800i";

		$query_args = array(
			'family' => urlencode( implode( '|', $font_families ) ),
			'subset' => urlencode( 'latin,latin-ext' ),
		);

        $u = "https://fonts.googleapis.com" . str_replace(" ", "-", strtolower($fontname));
		$fonts_url = add_query_arg( $query_args, $u );
	}

	return esc_url_raw( $fonts_url );
}

/**
 * Add preconnect for Google Fonts.
 *
 * @since Twenty Seventeen 1.0
 *
 * @param array  $urls           URLs to print for resource hints.
 * @param string $relation_type  The relation type the URLs are printed.
 * @return array $urls           URLs to print for resource hints.
 */
function twentyseventeen_resource_hints( $urls, $relation_type ) {
	if ( wp_style_is( 'twentyseventeen-fonts', 'queue' ) && 'preconnect' === $relation_type ) {
		$urls[] = array(
			'href' => 'https://fonts.gstatic.com',
			'crossorigin',
		);
	}

	return $urls;
}
add_filter( 'wp_resource_hints', 'twentyseventeen_resource_hints', 10, 2 );

